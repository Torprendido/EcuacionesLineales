public class Comp {

    public String asignacion;
    public int verdadera;
    public int falsa;
    public boolean lado;
    public String opR;

    public Comp(String asignacion, int verdadera, int falsa, boolean lado, String opR) {
        this.asignacion = asignacion;
        this.verdadera = verdadera;
        this.falsa = falsa;
        this.lado = lado;
        this.opR = opR;
    }
}
